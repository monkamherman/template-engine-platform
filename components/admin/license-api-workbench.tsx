"use client"

import { useMemo, useState } from "react"

import { Alert } from "@/components/ui/alert"
import { Badge } from "@/components/ui/badge"
import { Button } from "@/components/ui/button"
import { Card, CardContent, CardHeader, CardTitle } from "@/components/ui/card"
import { FormField } from "@/components/ui/form-field"
import { Input } from "@/components/ui/input"
import { Cluster } from "@/components/ui/layout"
import { Select } from "@/components/ui/select"
import { Textarea } from "@/components/ui/textarea"
import type { Locale } from "@/src/i18n/locales"

type Endpoint = "activate" | "validate" | "deactivate"

type ApiState = {
  status: number | null
  body: unknown
  error: string | null
}

const defaultInstallationId = "6df54c7c-e60a-4e31-8bdc-4ecf3b22bc4b"

export function LicenseApiWorkbench({ locale }: { locale: Locale }) {
  const isFrench = locale === "fr"
  const [licenseKey, setLicenseKey] = useState("")
  const [installationId, setInstallationId] = useState(defaultInstallationId)
  const [siteUrl, setSiteUrl] = useState("https://shop.example.test")
  const [environment, setEnvironment] = useState<"PRODUCTION" | "STAGING">("PRODUCTION")
  const [productSlug, setProductSlug] = useState("woo-app-theme")
  const [themeVersion, setThemeVersion] = useState("1.0.0")
  const [result, setResult] = useState<ApiState>({ status: null, body: null, error: null })
  const [loadingEndpoint, setLoadingEndpoint] = useState<Endpoint | null>(null)

  const payload = useMemo(
    () => ({
      licenseKey,
      installationId,
      siteUrl,
      environment,
      productSlug,
      themeVersion,
    }),
    [environment, installationId, licenseKey, productSlug, siteUrl, themeVersion],
  )

  async function callEndpoint(endpoint: Endpoint) {
    setLoadingEndpoint(endpoint)
    setResult({ status: null, body: null, error: null })

    try {
      const endpointPayload = endpoint === "deactivate" ? withoutThemeVersion(payload) : payload
      const response = await fetch(`/api/licenses/${endpoint}`, {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          Accept: "application/json",
          "X-TEP-Protocol": "1",
        },
        body: JSON.stringify(endpointPayload),
      })
      const body = (await response.json()) as unknown
      setResult({ status: response.status, body, error: null })
    } catch (error) {
      setResult({
        status: null,
        body: null,
        error: error instanceof Error ? error.message : "Unknown request failure",
      })
    } finally {
      setLoadingEndpoint(null)
    }
  }

  return (
    <main className="grid gap-6">
      <header className="rounded-xl border border-brand-border bg-white p-6 shadow-subtle">
        <Cluster>
          <Badge variant="orange">API V1</Badge>
          <Badge variant="outline">admin workbench</Badge>
          <Badge variant="outline">protocol header required</Badge>
        </Cluster>
        <h1 className="mt-5 font-heading text-3xl font-extrabold">
          {isFrench ? "Atelier API licences" : "License API workbench"}
        </h1>
        <p className="mt-3 max-w-3xl leading-7 text-brand-slate">
          {isFrench
            ? "Interface admin pour tester activate, validate et deactivate avec une cle de developpement generee par le seed."
            : "Admin interface for testing activate, validate and deactivate with a development key generated by the seed."}
        </p>
      </header>

      <Alert variant="info">
        {isFrench
          ? "Utilise uniquement des cles de developpement. La cle n'est pas placee dans l'URL et la reponse brute permet de verifier l'enveloppe protocole."
          : "Use development keys only. The key is not placed in the URL and the raw response lets you inspect the protocol envelope."}
      </Alert>

      <div className="grid gap-6 xl:grid-cols-[0.9fr_1.1fr]">
        <Card>
          <CardHeader>
            <CardTitle>{isFrench ? "Requete" : "Request"}</CardTitle>
          </CardHeader>
          <CardContent className="grid gap-4">
            <FormField
              hint={isFrench ? "Exemple: TEP1-.... genere par SEED_DEV_LICENSE=true pnpm db:seed" : "Example: TEP1-.... generated by SEED_DEV_LICENSE=true pnpm db:seed"}
              htmlFor="licenseKey"
              label={isFrench ? "Cle licence" : "License key"}
            >
              <Input
                autoComplete="off"
                id="licenseKey"
                onChange={(event) => setLicenseKey(event.target.value)}
                placeholder="TEP1-XXXX-XXXX-XXXX-XXXX-XXXX-XXXX-XXXX-XXXX"
                value={licenseKey}
              />
            </FormField>
            <div className="grid gap-4 md:grid-cols-2">
              <FormField htmlFor="installationId" label="Installation ID">
                <Input id="installationId" onChange={(event) => setInstallationId(event.target.value)} value={installationId} />
              </FormField>
              <FormField htmlFor="environment" label={isFrench ? "Environnement" : "Environment"}>
                <Select
                  id="environment"
                  onChange={(event) => setEnvironment(event.target.value as "PRODUCTION" | "STAGING")}
                  value={environment}
                >
                  <option value="PRODUCTION">PRODUCTION</option>
                  <option value="STAGING">STAGING</option>
                </Select>
              </FormField>
            </div>
            <FormField htmlFor="siteUrl" label="Site URL">
              <Input id="siteUrl" onChange={(event) => setSiteUrl(event.target.value)} value={siteUrl} />
            </FormField>
            <div className="grid gap-4 md:grid-cols-2">
              <FormField htmlFor="productSlug" label="Product slug">
                <Input id="productSlug" onChange={(event) => setProductSlug(event.target.value)} value={productSlug} />
              </FormField>
              <FormField htmlFor="themeVersion" label="Theme version">
                <Input id="themeVersion" onChange={(event) => setThemeVersion(event.target.value)} value={themeVersion} />
              </FormField>
            </div>
            <Cluster>
              {(["activate", "validate", "deactivate"] satisfies Endpoint[]).map((endpoint) => (
                <Button
                  disabled={!licenseKey || loadingEndpoint !== null}
                  isLoading={loadingEndpoint === endpoint}
                  key={endpoint}
                  onClick={() => void callEndpoint(endpoint)}
                  type="button"
                  variant={endpoint === "deactivate" ? "outline" : "primary"}
                >
                  {endpoint}
                </Button>
              ))}
            </Cluster>
          </CardContent>
        </Card>

        <Card>
          <CardHeader>
            <CardTitle>{isFrench ? "Reponse brute" : "Raw response"}</CardTitle>
          </CardHeader>
          <CardContent className="grid gap-4">
            <div className="flex flex-wrap gap-2">
              <Badge variant={result.status && result.status < 300 ? "success" : result.status ? "destructive" : "secondary"}>
                HTTP {result.status ?? "-"}
              </Badge>
              <Badge variant="outline">X-TEP-Protocol: 1</Badge>
            </div>
            {result.error ? <Alert variant="danger">{result.error}</Alert> : null}
            <Textarea
              className="min-h-[28rem] font-mono text-xs"
              readOnly
              value={result.body ? JSON.stringify(result.body, null, 2) : ""}
            />
          </CardContent>
        </Card>
      </div>
    </main>
  )
}

function withoutThemeVersion(payload: {
  environment: string
  installationId: string
  licenseKey: string
  productSlug: string
  siteUrl: string
  themeVersion: string
}) {
  return {
    licenseKey: payload.licenseKey,
    installationId: payload.installationId,
    siteUrl: payload.siteUrl,
    environment: payload.environment,
    productSlug: payload.productSlug,
  }
}
